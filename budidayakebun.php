<p style="text-align: justify;">
<strong>Permangkasan Dalam Budidaya Mangga</strong>
<br>Proses selanjutnya dari <em>Budidaya Mangga</em> setelah pemupukan beres, 
adalah tahap pemangkasan batang dan cabang Tanaman Mangga dengan tahap sebagai berikut :</p>
<ol style="text-align: justify;">
<li>Tahap Pertama</li>
</ol>
<p style="text-align: justify;">Pada usia 1 tahun pasca tanam dengan memotong 50cm batang dari permukaan tanah,
 dan juga pemotongan di atas bidang sambungan. Sisakan 3 cabang yang arahnya menyebar kesegala arah.</p>
<ol style="text-align: justify;" start="2">
<li>Tahap Kedua</li>
</ol>
<p style="text-align: justify;">Pangkaslah ketiga cabang yang disisakan tadi setelah berumur 2 tahun, sisakan 1-2 pupus. 
Kemudian tunas yang tumbuh pada setiap cabang dipelihara 3 tunas. Jika lebih dari itu segera dipangkas.
 Pada tahap ini maka akan diperoleh pohon dengan rumus cabang 1-3-9.</p>
<ol style="text-align: justify;" start="3">
<li>Tahap Ketiga</li>
</ol>
<p style="text-align: justify;">Pada tahap ini, lakukan cara yang sama seperti tahap kedua, 
namun tunas yang tumbuh biarkan dipelihara semua untuk menghasilkan pertumbuhan pohon Mangga yang sempurna.</p>
<ol style="text-align: justify;" start="4">
<li>Tahap Keempat</li>
</ol>
<p style="text-align: justify;">Pangkaslah setiap cabang pohon yang mengering atau mati
, cabang yang tumbuh kebawah atau kedalam, dan cabang air ( cabang muda yang tidak akan berbunga ) . 
Tahap pemangkasan ini dilaksanakan setelah masa panen.
<br>Setelah semua tahap dilalui dengan penuh semangat dan kerja keras,
 tibalah saatnya Pohon Mangga Anda berbunga, setelah berbunga, biasanya buah akan siap dipanen setelah 90 – 120 hari dari awal berbunga.</p>
                    
