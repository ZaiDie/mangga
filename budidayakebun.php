<center><h3>Cara Budidaya Mangga di Kebun</h3></center>
<p style="text-align: justify;"><strong>Persiapan Lahan Budidaya Mangga</strong>
<br>Hal pertama yang harus dilalukan dalam Budidaya Tanaman Mangga adalah mempersiapkan lahan, 
proses awal ini jangan dianggap sepele, sebab Pohon Mangga tidak akan tumbuh sempurna jika Anda menanamnya diatas tanah berbatu.
<br>langkah pertama mempersiapkan lahan budidaya mangga 
adalah sebagai berikut :</p>
<ol style="text-align: justify;">
<li> Lubangi tanah untuk budidaya mangga dengan cangkul, ukuran 1m x 1m x1m dengan jarak tanam 6m x 8m.
 Diamkan selama satu bulan agar sisa racun yang ada di tanah menguap tersapu angin.</li>
<li>Setelah satu bulan, tanah galian ditutup kembali dengan tanah yang sudah dicampur pupuk kandang, 
dan diamkan kembali selama dua minggu agar pupuk tercampur sempurna dan merata.</li>
<li>Setelah dua minggu, siapkan Bibit Mangga Unggul untuk persiapan menanam, 
waktu tanam yang paling baik dalam budidaya mangga adalah musim penghujan antara September – januari.</li>
</ol>
<p style="text-align: justify;"><strong>Proses Penanaman Mangga</strong>
<br>Setelah lahan Anda siap dan bibit sudah ditangan, tahap berikutnya dalam
budidaya mangga adalah :</p>
<ol style="text-align: justify;">
<li>Lepaskan kantong plastik atau polybag yang membungkus akar bibit mangga, jangan sampai ada sisa plastik yang tersisa dan ikut tertanam.</li>
<li>Lubangi tanah untuk budidaya mangga, kurang lebih 20cm diatas leher akar</li>
<li>Setelah bibit diletakkan kedalam lubang tanam, tutup dengan tanah dan ditekan kearah tanaman mangga, 
hal ini dimaksudkan agar tanaman tidak mudah roboh ketika hujan atau terkena angin kencang.</li>
<li>Naungi tanaman mangga tersebut dengan posisi miring ke barat,
 hal ini tujuannya agar bibit mangga muda tidak terlalu terpapar sinar matahari yang terlalu terik ketika siang hari.</li>
</ol>
