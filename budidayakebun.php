<ol style="text-align: justify;">
<li>Pada awal musim penghujan, biasanya dibulan September, berikan pupuk kandang. 
Caranya pemupukan dalam budidaya mangga adalah dengan benamkan pupuk kandang tersebut disekitar pohon selebar tajuk tanaman.
 Bisa juga dengan cara menggali lubang disekitar tanaman Mangga dan taburkan pupuk kandang tersebut di lubang tersebut kemudian ditutup 
 tanah kembali.</li>
<li>Setelah Mangga berusia 2 – 5 tahun, berikan lagi pupuk kandang dengan dosis 30kg, 
dan pada usia 6-15 tahun, dosis pupuk kandang yang diberikan untuk tanaman mangga adalah 60kg.</li>
<li>Untuk hasil lebih maksimal, dapat juga menggunakan pupuk Supernasa dengan dosis ½ sendok makan / 5 liter air per pohon Mangga.</li>
<li>Atau bisa juga dengan cara encerkan 1 botol Supernasa kedalam 2 liter air bersih, jadikan larutan tersebut sebagai larutan induk. 
Lalu setiap akan menyirami Pohon Mangga Anda, campurkan 20ml larutan induk tadi dengan 1 liter air bersih.</li>
<li>Untuk pemupukan berkala, berikan dosis larutan Supernasa diatas setiap 3-4 bulan sekali pada tanaman mangga Anda.</li>
<li>Selanjutnya agar Budidaya mangga Anda menuai hasil yang maksimal, 
bisa dilakukan penyemprotan POC NASA dengan dosis 4 tutup botol per tangki semprot setiap 2 bulan sekali.</li>
<li>Jangan lupa juga untuk menambahkan pupuk NPK 2 kali dalam setahun, yaitu diawal November-Desember, 
dan pada akhir musim penghujan yaitu antara April-Mei dengan dosis sebagai berikut :</li>
</ol>
