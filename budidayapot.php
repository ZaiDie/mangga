<p style="text-align: justify;"><strong>4. Penanaman bibit</strong><br/>Isi pot bagian dasar dengan menggunakan pacahan batu bata/genting atau juga bisa serutan gergaji dengan ketebalan 5-10 cm.hal ini bertujuan agar memperlancar drainase dan aliran udara.kemudian masukan campuran media ke dalam pot secara merata sehingga mencapai separuh pot.
Selanjutnya masukan bibit dengan posisi pas tengah-tengah pot, setelah itu timbun akar bibit dengan media campuran sampai mendekati permukaan atas pot. setelah tahapan selesai siram bibit tersebut dengan air.</p>
<p style="text-align: justify;"><strong>5. Pemeliharaan</strong><br/>untuk menjamin pertumbuhan dan pembuahan tanaman perawatan yang harus dilakukan meliputi penyiraman,pemangkasan,pemupukan, serta pengendalian hama dan penyakit.</p>
<p style="text-align: justify;"><strong>6. Penyiraman</strong><br/>Lakukan penyiraman dengan baik dan teratur, jangan berlebihan karena akan membuat media yang ada didalam pot akan hanyut keluar bersama air melalui lubang buat. lakukan penyiraman pada awal tanam agar bibit cepat hidup didalam pot, penyiraman yang baik dilakukan pada pagi dan sore hari.</p>
<p style="text-align: justify;"><strong>7. Pemupukan</strong><br/>untuk pemupukan bisa menggunakan pupuk organik maupun anorganik, 
pupuk organik seperti pupuk kompos dan pupuk kandang sedangkan pupuk anorganik seperti NPK,urea, dll.<br>
untuk mengaplikasikan pupuk dengan tanaman adalah dengan cara ditaburkan di media tanam disekitar tanaman kemudian disiram dengan air.</p>
<p style="text-align: justify;"><strong>8. Penanggulangan hama dan penyakit</strong><br/>
								jenis hama yang sering menyerang tanaman mangga seperti bisul,lalat,kutu,ulat,dan binatang penggerek,dll.<br>
								untuk menanggulangi hama diatas gunakan pestisida dengan cara disemprotkan.<br>catatan : penyemprotan dengan pestisida boleh dilakukan apabila mangga benar-benar telah terserang hama</p>
<p style="text-align: justify;"><strong>9. Pemanenan</strong><br />
								panen mangga paling besar biasanya jatuh pada bulan September-Oktober, 
								untuk mangga dari cangkokan biasanya berbuah pada umur 4 th sedangkan mangga okulasi berbuah pada umur 5-6 th.</p>
<p style="text-align: justify;">ciri-ciri buah mangga yang siap dipanen adalah adanya buah mangga yang jatuh karena matang.sedangkan cara memanen mangga. lakukan pemetikan buah pada sore hari gunakan pisau yang tajam usahakan buah jangan sampai terpotong, tercongkel, atau jatuh memar.</p>
