<center><h3>Cara Budidaya Mangga di Pot</h3></center>

<p style="text-align: justify;">Budidaya Menggunakan wadah pot dibuat untuk para pembudidaya yang kekurangan atau tidak ada lahan
penanaman mangga. berikut langkah budidaya mangga didalam pot agar rajin berbuah.</p>
<p style="text-align: justify;"><strong>1. Pemilihan bibit</strong><br/>sebaiknya pilih bibit yang bervarietas dan mudah berbuah. seperti mangga arumanis dan mangga manalagi. jika anda ingin mangga cepat berbuah sebaiknya pilih bibit dari mencangkok atau okulasi.</p>
<p style="text-align: justify;"><strong>2. Media tanam</strong><br/>Dalam budidaya mangga dalam pot sebaiknya anda pilih media tanam bertekstur remah, gembur, berbutir dan kaya unsur hara media juga harus porous, cukup ringan agar mudah apabila dipindahkan. selain itu media juga harus bebas dari hama dan penyakit.</p>
<p>gunakan komposisi bahan yang digunakan seperti campuran tanah, pupuk kompos/pupuk kandang serta arang sekam. dengan perbandingan<br />
2:1:1.</p>
<p style="text-align: justify;"><strong>3. Pemilihan pot</strong><br/>Banyak jenis pot yang bisa digunakan untuk budidaya mangga, tetapi alangkah baiknya pilih pot yang ringan, tidah mudah pecah,bentuknya normal, serta sesuai dengan tanaman.</p>
<p>kriteria pot yang baik untuk budidaya mangga dalam pot<br />
&#8211; bagian bawah pot terlebih dahulu harus dilubangi<br />
&#8211; ukuran lubang 1-2 cm<br />
&#8211; dalam satu pot beri lubang sebanyak 4-6 bh<br />
&#8211; sebaiknya pot memiliki kaki supaya pembuangan lebih lancar</p>
